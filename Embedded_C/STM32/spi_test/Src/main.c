int main(void)
{

  /* USER CODE BEGIN 1 */
	uint8_t status;
	uint8_t replay[4];
//	char msg[50];
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_SPI1_Init();
  MX_ADC1_Init();
  MX_ADC2_Init();
  MX_TIM2_Init();

  /* USER CODE BEGIN 2 */
	power_up_reset();
	
	status = module_init();
	
	issue_strobe(STX, RW_WRITING, &status);
	issue_strobe(SNOP, RW_WRITING, &status);
	
	status = read_register(0x00, replay, 4);
	
	CarControlInit();
	
	(cc_buffer_init(&buffer, 0))->next = buffer;
	read = write = buffer;
	
	__HAL_TIM_ENABLE_IT(&htim2, TIM_IT_UPDATE);
	__HAL_TIM_ENABLE(&htim2);
	
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
		if(read->status == BUSY){
			
			status = fifo_access((uint8_t *)&(read->payload), 5, RW_WRITING);
			read->status = FREE;
			read = read->next;
		}
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
		
  }
  /* USER CODE END 3 */

}

