void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	int16_t xy[2];

	
	if(GetValueX_diff(&(xy[0])) | GetValueY_diff(&(xy[1])))
	{		
		if(write->status == FREE){
			
			write->status = BUSY;
			write->payload = CarModel;
			
			write = write->next;
			
		}
	}
	HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_15);
}

void CarCalibrate(float32_t *x, float32_t *y)
{
	uint8_t iterator = 0;
	float32_t Quantity[50];
	
	while(iterator < 50)
	{
		HAL_ADC_Start(&hadc2);
		while(__HAL_ADC_GET_FLAG(&hadc2, ADC_FLAG_EOC) != SET) {}
		Quantity[iterator++] = (HAL_ADC_GetValue(&hadc2) >> 5 );
	}
	
	arm_mean_f32(Quantity, 50, x);
	
	iterator = 0;
	
	while(iterator < 50)
	{
		HAL_ADC_Start(&hadc1);
		while(__HAL_ADC_GET_FLAG(&hadc1, ADC_FLAG_EOC) != SET) {}
		Quantity[iterator++] = (HAL_ADC_GetValue(&hadc1) >> 5 );
	}
	
	arm_mean_f32(Quantity, 50, y);
}

